function formatMessage(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match,
  );
}

export function t(key: string, params?: TranslationParams): string {
  let current: Locale = "en";
  locale.subscribe((value) => (current = value))();
  return formatMessage(messages[current][key] ?? messages.en[key] ?? key, params);
}

export const translator = derived(locale, (current) => {
  return (key: string, params?: TranslationParams) =>
    formatMessage(messages[current][key] ?? messages.en[key] ?? key, params);
});


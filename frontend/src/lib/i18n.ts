export function translateError(
  error: unknown,
  translate: Translator,
  fallbackKey: string,
): string {
  const value = error as { code?: string; message?: string } | null;
  const code = value?.code ?? value?.message ?? "";
  return translate(ERROR_KEYS[code] ?? fallbackKey);
}

export function localizeNotification(
  item: { type: string; message: string },
  translate: Translator,
): { title: string; message: string } {
  const keys: Record<string, [string, string]> = {
    reply: ["notifications.event.newReplyTitle", "notifications.event.newReplyMessage"],
    vote: ["notifications.event.newVoteTitle", "notifications.event.newVoteMessage"],
    vote_passed: ["notifications.event.votePassedTitle", "notifications.event.votePassedMessage"],
    vote_rejected: ["notifications.event.voteRejectedTitle", "notifications.event.voteRejectedMessage"],
    patch_merged: ["notifications.event.patchMergedTitle", "notifications.event.patchMergedMessage"],
    patch_failed: ["notifications.event.patchFailedTitle", "notifications.event.patchFailedMessage"],
    moderation_pending: ["notifications.event.moderationPendingTitle", "notifications.event.moderationPendingMessage"],
    moderation_approved: ["notifications.event.moderationApprovedTitle", "notifications.event.moderationApprovedMessage"],
    moderation_rejected: ["notifications.event.moderationRejectedTitle", ""],
    following_post: ["notifications.event.followingPostTitle", ""],
    following_patch: ["notifications.event.followingPatchTitle", ""],
  };
  const [titleKey, messageKey] = keys[item.type] ?? [
    "notifications.event.updateTitle",
    "notifications.event.updateMessage",
  ];
  if (item.type === "moderation_pending") {
    return {
      title: translate(titleKey),
      message: translate(
        item.message.trim() === "classifier_unavailable"
          ? "notifications.event.moderationPendingUnavailableMessage"
          : messageKey,
      ),
    };
  }
  if (item.type === "moderation_rejected") {
    const note = item.message.trim();
    return {
      title: translate(titleKey),
      message: note
        ? translate("notifications.event.moderationRejectedMessage", { note })
        : translate("notifications.event.moderationRejectedFallbackMessage"),
    };
  }
  if (item.type === "moderation_approved") {
    const note = item.message.trim();
    return {
      title: translate(titleKey),
      message: note
        ? translate("notifications.event.moderationApprovedNoteMessage", { note })
        : translate(messageKey),
    };
  }
  return {
    title: translate(titleKey),
    message: messageKey ? translate(messageKey) : item.message,
  };
}

export function getLocaleLabel(value: Locale): string {
  return ({ en: "English", ja: "日本語", "zh-TW": "繁體中文" })[value];
}

export function initI18n() {
  if (typeof window === "undefined") return;
  let saved: Locale | null = null;
  try {
    saved = window.localStorage.getItem(STORAGE_KEY) as Locale | null;
  } catch {}
  const next: Locale = saved === "ja" || saved === "zh-TW" ? saved : "en";
  locale.set(next);
  document.documentElement.lang = next === "zh-TW" ? "zh-Hant-TW" : next;
  updateDocumentTitle(next);
}

export function setLocale(next: Locale) {
  locale.set(next);
  if (typeof window !== "undefined") {
    try {
      window.localStorage.setItem(STORAGE_KEY, next);
    } catch {}
    document.documentElement.lang = next === "zh-TW" ? "zh-Hant-TW" : next;
    updateDocumentTitle(next);
  }
}

function updateDocumentTitle(current: Locale) {
  const key = document.documentElement.dataset.titleKey;
  if (!key) return;
  const pageTitle = messages[current][key] ?? messages.en[key];
  if (pageTitle) document.title = `${pageTitle} · Agora`;
}
